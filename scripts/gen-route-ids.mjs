// Generates src/lib/content-route-ids.ts from the actual content collections so
// the typed link registry (src/lib/links.ts) has REAL literal-union route ids
// that cannot drift from content.
//
// Why this exists: Astro's public `astro:content` types (CollectionEntry<C>['id'|'slug'])
// collapse to `string` in this version -- the literal-bearing internal entry maps
// (ContentEntryMap/DataEntryMap) are not exported -- so deriving narrow ids from the
// generated types is impossible. This tiny codegen reads the content dirs instead.
// Node built-ins only; no new dependency. Runs before `astro build` (see package.json).
//
// Astro id semantics for these flat collections: the route id is the file path
// relative to the collection dir, without extension (articles use the slug, which
// for our un-customised config equals that same value).

import { readdirSync, writeFileSync } from 'node:fs';
import { join, relative, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const contentDir = join(here, '..', 'src', 'content');
const outFile = join(here, '..', 'src', 'lib', 'content-route-ids.ts');

// Collect entry ids (path relative to the collection dir, extension stripped).
function idsFor(collection) {
  const dir = join(contentDir, collection);
  const files = readdirSync(dir, { recursive: true, withFileTypes: true })
    .filter((d) => d.isFile())
    .map((d) => join(d.parentPath ?? d.path, d.name));
  return files
    .map((f) => relative(dir, f).replace(/\\/g, '/').replace(/\.[^.]+$/, ''))
    .sort();
}

const articleIds = idsFor('articles');
const calculatorIds = idsFor('calculators');
const categoryIds = idsFor('categories');

const arr = (name, ids) =>
  `export const ${name} = [${ids.map((id) => `'${id}'`).join(', ')}] as const;`;

const out = `// AUTO-GENERATED by scripts/gen-route-ids.mjs -- do not edit by hand.
// Regenerated on every \`npm run build\` from the src/content/** collections.
${arr('articleIds', articleIds)}
${arr('calculatorIds', calculatorIds)}
${arr('categoryIds', categoryIds)}

export type ArticleContentId = (typeof articleIds)[number];
export type CalculatorContentId = (typeof calculatorIds)[number];
export type CategoryContentId = (typeof categoryIds)[number];
`;

writeFileSync(outFile, out, 'utf8');
console.log(
  `gen-route-ids: wrote ${articleIds.length} article, ${calculatorIds.length} calculator, ${categoryIds.length} category ids to src/lib/content-route-ids.ts`,
);
